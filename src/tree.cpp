#include "tree.hpp"

/* ========================================================================== */
/*                                   PUBLIC                                   */
/* ========================================================================== */

Tree::Tree():
	_head(nullptr) {}

Tree::~Tree() {}

Tree::Tree(const Tree& x):
	_head(std::move(x._head->toNode())) {}

Tree::Tree(unique_node&& head):
	_head(std::move(head)) {}


/* Main Functions ----------------------------------------------------------- */

void	Tree::print() const {
	return _head->print();
}

Tree::shared_itype	Tree::eval() const {
	return _head->eval();
}